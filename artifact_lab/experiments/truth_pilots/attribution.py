"""Agent attribution signals in commits touching AI instruction files."""

from __future__ import annotations

import re
from dataclasses import dataclass

CO_AUTHORED_BY = re.compile(r"Co-Authored-By:\s*(.+)", re.IGNORECASE | re.MULTILINE)

BOT_AUTHOR_PATTERNS: tuple[re.Pattern[str], ...] = (
    re.compile(r"\[bot\]", re.IGNORECASE),
    re.compile(r"dependabot", re.IGNORECASE),
    re.compile(r"github-actions", re.IGNORECASE),
    re.compile(r"renovate", re.IGNORECASE),
    re.compile(r"cursoragent", re.IGNORECASE),
    re.compile(r"cursor[- ]?agent", re.IGNORECASE),
)

AGENT_SIGNATURE_PATTERNS: tuple[tuple[str, re.Pattern[str]], ...] = (
    ("claude", re.compile(r"\bclaude\b|\banthropic\b", re.IGNORECASE)),
    ("cursor", re.compile(r"\bcursor\b|\bcursoragent\b|\bcursor agent\b", re.IGNORECASE)),
    ("copilot", re.compile(r"\bcopilot\b|\bgithub copilot\b", re.IGNORECASE)),
    ("openai", re.compile(r"\bopenai\b|\bgpt-[\w-]+\b", re.IGNORECASE)),
    ("aider", re.compile(r"\baider\b", re.IGNORECASE)),
    ("devin", re.compile(r"\bdevin\b", re.IGNORECASE)),
    ("codeium", re.compile(r"\bcodeium\b", re.IGNORECASE)),
)

ATTRIBUTION_CLASSES = (
    "human",
    "agent_coauthored",
    "bot_author",
    "agent_signature_in_message",
)


@dataclass(frozen=True)
class AttributionCandidate:
    repo_id: str
    repo_url: str
    instruction_path: str
    family_group: str
    commit_sha: str
    commit_time: str
    author_name: str
    author_email: str
    attribution_class: str
    signature_type: str
    evidence: str


def _author_is_bot(author_name: str, author_email: str) -> bool:
    combined = f"{author_name} {author_email}"
    return any(pat.search(combined) for pat in BOT_AUTHOR_PATTERNS)


def _co_authored_agents(message: str) -> list[str]:
    hits: list[str] = []
    for match in CO_AUTHORED_BY.finditer(message):
        line = match.group(1).strip()
        for label, pat in AGENT_SIGNATURE_PATTERNS:
            if pat.search(line):
                hits.append(f"co_authored_by:{label}:{line[:120]}")
        if not hits or not hits[-1].endswith(line[:120]):
            if "bot" in line.lower() or "cursor" in line.lower() or "copilot" in line.lower():
                hits.append(f"co_authored_by:unknown:{line[:120]}")
    return hits


def _message_signatures(message: str) -> list[str]:
    hits: list[str] = []
    for label, pat in AGENT_SIGNATURE_PATTERNS:
        match = pat.search(message)
        if match:
            hits.append(f"message_signature:{label}:{match.group(0)}")
    generated = re.search(
        r"(?i)(generated by|written by|created with|assisted by)\s+[\w .-]{3,40}",
        message,
    )
    if generated:
        hits.append(f"tool_pattern:phrase:{generated.group(0)[:80]}")
    return hits


def classify_commit(
    *,
    author_name: str,
    author_email: str,
    commit_message: str,
) -> tuple[str, str, str]:
    coauth = _co_authored_agents(commit_message)
    if coauth:
        return "agent_coauthored", "co_authored_by", "; ".join(coauth[:3])

    if _author_is_bot(author_name, author_email):
        return "bot_author", "bot_account", f"author={author_name} <{author_email}>"

    signatures = _message_signatures(commit_message)
    if signatures:
        sig_type = signatures[0].split(":", 1)[0]
        return "agent_signature_in_message", sig_type, "; ".join(signatures[:3])

    return "human", "none", "no agent signal detected"


def scan_commit_attribution(
    *,
    repo_id: str,
    repo_url: str,
    instruction_path: str,
    family_group: str = "",
    commit_sha: str,
    commit_time: str,
    author_name: str,
    author_email: str,
    commit_message: str,
) -> AttributionCandidate:
    attribution_class, signature_type, evidence = classify_commit(
        author_name=author_name,
        author_email=author_email,
        commit_message=commit_message,
    )
    return AttributionCandidate(
        repo_id=repo_id,
        repo_url=repo_url,
        instruction_path=instruction_path,
        family_group=family_group,
        commit_sha=commit_sha,
        commit_time=commit_time,
        author_name=author_name,
        author_email=author_email,
        attribution_class=attribution_class,
        signature_type=signature_type,
        evidence=evidence,
    )
